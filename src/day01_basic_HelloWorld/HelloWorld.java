//声明包
package day01_basic_HelloWorld;
//main为主方法，为程序入口
public class HelloWorld { //声明类
	//程序从main方法执行，也从main方法结束
	public static void main(String[] args) {
		//打印信息
		/*1、java是严格区分大小写的
		  2、符号必须是英文模式
		  3、每句话必须以;号结束
		  4、println()输出并换行，print()输出不换行
		*/
		System.out.println("输出并换行!");
		System.out.print("输出不换行!");

	}

}
